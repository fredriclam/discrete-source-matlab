% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Grab the strings of variables selected
s1 = get(handles.popupmenu1,'UserData');
s1 = char(s1);
s2 = get(handles.popupmenu2,'UserData');
s2 = char(s2);
% Generate new plot object
plot_fig = struct;
plot_fig.fig_handle = figure(77);
plot_fig.axes_handle = gca;

% Generate plot and component objects
plot_fig.plot_handle = plot(evalin('base',s1), evalin('base',s2));
% Title
str = char(get(handles.edit3,'String'));
% if ~strcmp(str, '')
plot_fig.title_handle = title(str);
% end
% Axes
str = char(get(handles.edit4,'String'));
% if ~strcmp(str, '')
plot_fig.xlabel_handle = xlabel(str);
% end
str = char(get(handles.edit5,'String'));
% if ~strcmp(str, '')
plot_fig.ylabel_handle = ylabel(str);
% end

% Save figure handle
set(handles.pushbutton1, 'UserData', plot_fig);

% Set aspect ratio
% set(plot_fig.axes_handle, 'DataAspectRatio',[1 1 1])

% Set axis ticks
str = char(get(handles.edit7,'String'));
num = 5;
if ~strcmp(str, '')
    num = str2num(str);
end
xlim = get(plot_fig.axes_handle,'XLim');
set(plot_fig.axes_handle,'XTick',linspace(xlim(1), xlim(2),num));
% (y-axis)
str = char(get(handles.edit8,'String'));
num = 5;
if ~strcmp(str, '')
    num = str2num(str);
end
ylim = get(plot_fig.axes_handle,'YLim');
set(plot_fig.axes_handle,'YTick',linspace(ylim(1), ylim(2),num));
% Set minor ticks
if ~get(handles.checkbox1,'Value')
    set(plot_fig.axes_handle,'XMinorTick','on');
end
if ~get(handles.checkbox2,'Value')
    set(plot_fig.axes_handle,'YMinorTick','on');
end

% Read font, size
str = get(handles.popupmenu4, 'String');
font_name = char(str(get(handles.popupmenu4, 'Value')));
font_size_large = str2num(char(get(handles.edit19, 'String')));
font_size = str2num(char(get(handles.edit20, 'String')));
% Set font, size
set(plot_fig.axes_handle,'FontName', font_name,...
    'FontSize', font_size);
set(plot_fig.xlabel_handle,'FontName', font_name,...
    'FontSize', font_size_large);
set(plot_fig.ylabel_handle,'FontName', font_name,...
    'FontSize', font_size_large);
set(plot_fig.title_handle,'FontName', font_name,...
    'FontSize', font_size_large);

